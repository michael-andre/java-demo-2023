package fr.centralesupelec.sio.demo.shapes;

/**
 * A simple class to hold two {x,y} coordinates.
 */
public class Point {

    // We chose to simplify things be only considering integer coordinates.
    private int x, y;

    public Point(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }

    @Override
    public String toString() {
        return "Point{" +
                "x=" + x +
                ", y=" + y +
                '}';
    }

}
